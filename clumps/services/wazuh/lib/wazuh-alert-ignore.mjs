import { flagGet } from "../../../lib/parse-argv-flags.mjs";

export const WAZUH_MONITOR_LIST_NAME = "hdc-monitor-sources";
export const WAZUH_MONITOR_LIST_REL = `etc/lists/${WAZUH_MONITOR_LIST_NAME}`;
export const WAZUH_MONITOR_RULE_ID = 100100;

/** @type {string[]} */
export const WAZUH_ALERT_IGNORE_DEFAULT_GROUPS = [
  "web",
  "web_scan",
  "attack",
  "ids",
  "modsecurity",
  "nginx",
];

/**
 * @param {Record<string, string>} [flags]
 */
export function wazuhAlertIgnoreSkippedByFlags(flags) {
  return flagGet(flags ?? {}, "skip-alert-ignore", "skip_alert_ignore") !== undefined;
}

/**
 * @param {unknown} v
 */
function isObject(v) {
  return v !== null && typeof v === "object" && !Array.isArray(v);
}

/**
 * @param {string} ip
 */
function isPlausibleIp(ip) {
  const t = ip.trim();
  if (!t) return false;
  // IPv4 or IPv6 (basic); reject paths / whitespace mid-string
  if (/\s/.test(t)) return false;
  if (/^(\d{1,3}\.){3}\d{1,3}$/.test(t)) return true;
  if (t.includes(":")) return true;
  return false;
}

/**
 * @typedef {{ srcips: string[]; groups: string[] }} WazuhAlertIgnoreSettings
 */

/**
 * Resolve alert_ignore from a wazuh deployment/defaults object.
 * Empty srcips → null (no-op).
 *
 * @param {Record<string, unknown>} wazuh
 * @returns {WazuhAlertIgnoreSettings | null}
 */
export function resolveWazuhAlertIgnore(wazuh) {
  if (!isObject(wazuh)) return null;
  const raw = isObject(wazuh.alert_ignore) ? wazuh.alert_ignore : null;
  if (!raw) return null;
  const srcips = Array.isArray(raw.srcips)
    ? [...new Set(raw.srcips.map((x) => String(x).trim()).filter(isPlausibleIp))]
    : [];
  if (!srcips.length) return null;
  const groups = Array.isArray(raw.groups)
    ? [...new Set(raw.groups.map((x) => String(x).trim()).filter(Boolean))]
    : [];
  return {
    srcips,
    groups: groups.length ? groups : [...WAZUH_ALERT_IGNORE_DEFAULT_GROUPS],
  };
}

/**
 * CDB list body for address_match_key lookups (one `IP:` per line).
 *
 * @param {string[]} srcips
 */
export function renderWazuhMonitorCdbList(srcips) {
  const lines = srcips.map((ip) => `${ip}:`);
  return `${lines.join("\n")}\n`;
}

/**
 * Custom local_rules.xml that mutes configured groups when srcip is in the CDB list.
 *
 * @param {WazuhAlertIgnoreSettings} settings
 */
export function renderWazuhMonitorLocalRules(settings) {
  const groupExpr = settings.groups.join("|");
  return [
    "<!-- Generated by hdc wazuh maintain — do not edit by hand -->",
    '<group name="hdc,">',
    `  <rule id="${WAZUH_MONITOR_RULE_ID}" level="0">`,
    `    <if_group>${groupExpr}</if_group>`,
    `    <list field="srcip" lookup="address_match_key">${WAZUH_MONITOR_LIST_REL}</list>`,
    "    <description>HDC: ignore web/attack noise from trusted monitoring sources</description>",
    "  </rule>",
    "</group>",
    "",
  ].join("\n");
}

/**
 * Ensure `<list>etc/lists/hdc-monitor-sources</list>` exists under `<ruleset>`.
 *
 * @param {string} confText
 * @param {string} [listRel]
 */
export function patchWazuhManagerConfMonitorList(confText, listRel = WAZUH_MONITOR_LIST_REL) {
  const listTag = `<list>${listRel}</list>`;
  if (confText.includes(listTag)) return confText;
  if (/<ruleset\b[^>]*>[\s\S]*?<\/ruleset>/i.test(confText)) {
    return confText.replace(/<\/ruleset>/i, `  ${listTag}\n  </ruleset>`);
  }
  // Insert a minimal ruleset block before closing ossec_config when missing
  if (/<\/ossec_config>/i.test(confText)) {
    return confText.replace(
      /<\/ossec_config>/i,
      `  <ruleset>\n    ${listTag}\n  </ruleset>\n</ossec_config>`,
    );
  }
  return `${confText.trimEnd()}\n<ossec_config>\n  <ruleset>\n    ${listTag}\n  </ruleset>\n</ossec_config>\n`;
}

/**
 * Ensure each srcip has a `<white_list>` entry under `<global>`.
 *
 * @param {string} confText
 * @param {string[]} srcips
 */
export function patchWazuhManagerConfWhiteList(confText, srcips) {
  let text = confText;
  const missing = srcips.filter((ip) => {
    const re = new RegExp(`<white_list>\\s*${escapeRegExp(ip)}\\s*</white_list>`, "i");
    return !re.test(text);
  });
  if (!missing.length) return text;
  const insert = missing.map((ip) => `    <white_list>${ip}</white_list>`).join("\n");
  if (/<\/global>/i.test(text)) {
    return text.replace(/<\/global>/i, `${insert}\n  </global>`);
  }
  if (/<\/ossec_config>/i.test(text)) {
    return text.replace(
      /<\/ossec_config>/i,
      `  <global>\n${insert}\n  </global>\n</ossec_config>`,
    );
  }
  return `${text.trimEnd()}\n<ossec_config>\n  <global>\n${insert}\n  </global>\n</ossec_config>\n`;
}

/**
 * @param {string} confText
 * @param {WazuhAlertIgnoreSettings} settings
 */
export function patchWazuhManagerConfAlertIgnore(confText, settings) {
  let text = patchWazuhManagerConfMonitorList(confText);
  text = patchWazuhManagerConfWhiteList(text, settings.srcips);
  return text;
}

/**
 * @param {string} s
 */
function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

/**
 * Bash fragment: write CDB list + local_rules, patch manager conf, restart manager.
 * Expects STACK in the environment. No-op when settings is null/empty.
 *
 * @param {WazuhAlertIgnoreSettings | null} settings
 */
export function buildWazuhAlertIgnoreSyncBash(settings) {
  if (!settings?.srcips?.length) {
    return "echo 'wazuh alert_ignore skipped: no srcips configured'";
  }
  const payload = {
    list_name: WAZUH_MONITOR_LIST_NAME,
    list_rel: WAZUH_MONITOR_LIST_REL,
    cdb: renderWazuhMonitorCdbList(settings.srcips),
    local_rules: renderWazuhMonitorLocalRules(settings),
    srcips: settings.srcips,
  };
  const payloadJson = JSON.stringify(payload);
  return [
    "python3 - <<'PY'",
    "from pathlib import Path",
    "import json",
    "import os",
    "import re",
    "import subprocess",
    "import time",
    "",
    `payload = json.loads(${JSON.stringify(payloadJson)})`,
    "stack = Path(os.environ['STACK'])",
    "conf_path = stack / 'config' / 'wazuh_cluster' / 'wazuh_manager.conf'",
    "container = 'single-node-wazuh.manager-1'",
    "",
    "def docker_exec(args, input_text=None):",
    "  cmd = ['docker', 'exec', '-i' if input_text is not None else '', container]",
    "  cmd = [c for c in cmd if c]",
    "  return subprocess.run(cmd + args, input=input_text, text=True, check=False, capture_output=True)",
    "",
    "def ensure_list_tag(text, list_rel):",
    "  tag = f'<list>{list_rel}</list>'",
    "  if tag in text:",
    "    return text",
    "  if re.search(r'</ruleset>', text, flags=re.I):",
    "    return re.sub(r'</ruleset>', f'  {tag}\\n  </ruleset>', text, count=1, flags=re.I)",
    "  if re.search(r'</ossec_config>', text, flags=re.I):",
    "    block = f'  <ruleset>\\n    {tag}\\n  </ruleset>\\n</ossec_config>'",
    "    return re.sub(r'</ossec_config>', block, text, count=1, flags=re.I)",
    "  return text + f'\\n<ossec_config>\\n  <ruleset>\\n    {tag}\\n  </ruleset>\\n</ossec_config>\\n'",
    "",
    "def ensure_white_lists(text, ips):",
    "  missing = []",
    "  for ip in ips:",
    "    if not re.search(rf'<white_list>\\s*{re.escape(ip)}\\s*</white_list>', text, flags=re.I):",
    "      missing.append(ip)",
    "  if not missing:",
    "    return text",
    "  insert = '\\n'.join(f'    <white_list>{ip}</white_list>' for ip in missing)",
    "  if re.search(r'</global>', text, flags=re.I):",
    "    return re.sub(r'</global>', f'{insert}\\n  </global>', text, count=1, flags=re.I)",
    "  if re.search(r'</ossec_config>', text, flags=re.I):",
    "    block = f'  <global>\\n{insert}\\n  </global>\\n</ossec_config>'",
    "    return re.sub(r'</ossec_config>', block, text, count=1, flags=re.I)",
    "  return text + f'\\n<ossec_config>\\n  <global>\\n{insert}\\n  </global>\\n</ossec_config>\\n'",
    "",
    "if conf_path.is_file():",
    "  conf_text = conf_path.read_text()",
    "  conf_text = ensure_list_tag(conf_text, payload['list_rel'])",
    "  conf_text = ensure_white_lists(conf_text, payload['srcips'])",
    "  conf_path.write_text(conf_text)",
    "  print('wazuh_manager.conf alert_ignore patched')",
    "else:",
    "  print('warning: missing wazuh_manager.conf; continuing with in-container patch only')",
    "",
    "for _ in range(36):",
    "  ps = subprocess.run(['docker', 'inspect', '-f', '{{.State.Running}}', container], text=True, capture_output=True)",
    "  running = ps.stdout.strip().lower() == 'true'",
    "  if not running:",
    "    # Compose service name fallback while the container is (re)creating",
    "    cps = subprocess.run(['docker', 'compose', 'ps', '--status', 'running', '-q', 'wazuh.manager'], text=True, capture_output=True)",
    "    running = bool(cps.stdout.strip())",
    "  if running:",
    "    # Confirm exec works before writing files",
    "    probe = docker_exec(['true'])",
    "    if probe.returncode == 0:",
    "      break",
    "  time.sleep(5)",
    "else:",
    "  raise SystemExit('wazuh manager container not running')",
    "",
    "list_dir = '/var/ossec/etc/lists'",
    "list_path = f\"{list_dir}/{payload['list_name']}\"",
    "rules_path = '/var/ossec/etc/rules/local_rules.xml'",
    "ossec_conf = '/var/ossec/etc/ossec.conf'",
    "",
    "w = docker_exec(['sh', '-c', f'umask 027; mkdir -p {list_dir}; cat > {list_path}; chown root:wazuh {list_path}; chmod 640 {list_path}'], input_text=payload['cdb'])",
    "if w.returncode != 0:",
    "  raise SystemExit(w.stderr or w.stdout or 'failed to write monitor CDB list')",
    "",
    "w = docker_exec(['sh', '-c', f'umask 027; cat > {rules_path}; chown root:wazuh {rules_path}; chmod 640 {rules_path}'], input_text=payload['local_rules'])",
    "if w.returncode != 0:",
    "  raise SystemExit(w.stderr or w.stdout or 'failed to write local_rules.xml')",
    "",
    "r = docker_exec(['cat', ossec_conf])",
    "if r.returncode != 0:",
    "  raise SystemExit(r.stderr or r.stdout or 'failed to read ossec.conf')",
    "inner = ensure_list_tag(r.stdout, payload['list_rel'])",
    "inner = ensure_white_lists(inner, payload['srcips'])",
    "w = docker_exec(['sh', '-c', f'umask 027; cat > {ossec_conf}'], input_text=inner)",
    "if w.returncode != 0:",
    "  raise SystemExit(w.stderr or w.stdout or 'failed to write ossec.conf')",
    "",
    "subprocess.run(['docker', 'restart', container], check=True)",
    "for _ in range(24):",
    "  chk = docker_exec(['sh', '-c', f'test -s {list_path} && test -s {rules_path} && grep -q hdc-monitor-sources {ossec_conf}'])",
    "  if chk.returncode == 0:",
    "    print('wazuh alert_ignore synced (%d srcips)' % len(payload['srcips']))",
    "    break",
    "  time.sleep(5)",
    "else:",
    "  raise SystemExit('alert_ignore files not ready after manager restart')",
    "PY",
  ].join("\n");
}
