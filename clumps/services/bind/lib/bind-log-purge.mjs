import { flagGet } from "../../../lib/parse-argv-flags.mjs";
import { staggerOffsetFromSystemId } from "../../../lib/guest-systemd-unit-ensure.mjs";

export const DEFAULT_THRESHOLD_FREE_PERCENT = 15;
export const BIND_LOG_PURGE_SCRIPT_PATH = "/usr/local/sbin/hdc-bind-log-purge";
export const BIND_LOG_PURGE_CRON_PATH = "/etc/cron.d/hdc-bind-log-purge";
export const BIND_LOG_PURGE_RUN_LOG = "/var/log/hdc-bind-log-purge.log";

/** @param {unknown} v */
function isObject(v) {
  return v !== null && typeof v === "object" && !Array.isArray(v);
}

/**
 * @param {Record<string, string>} [flags]
 */
export function logPurgeSkippedByFlags(flags) {
  return flagGet(flags ?? {}, "skip-log-purge", "skip_log_purge") !== undefined;
}

/**
 * @param {unknown} bindBlock
 * @returns {{ enabled: boolean; thresholdFreePercent: number }}
 */
export function resolveBindLogPurgeSettings(bindBlock) {
  const bind = isObject(bindBlock) ? bindBlock : {};
  const logPurge = isObject(bind.log_purge) ? bind.log_purge : {};
  const enabled = logPurge.enabled !== false;
  const raw =
    typeof logPurge.threshold_free_percent === "number"
      ? logPurge.threshold_free_percent
      : Number(logPurge.threshold_free_percent);
  const thresholdFreePercent =
    Number.isFinite(raw) && raw > 0 && raw < 100 ? Math.floor(raw) : DEFAULT_THRESHOLD_FREE_PERCENT;
  return { enabled, thresholdFreePercent };
}

/**
 * @param {object} [opts]
 * @param {number} [opts.thresholdFreePercent]
 */
export function buildBindLogPurgeScript(opts = {}) {
  const threshold =
    typeof opts.thresholdFreePercent === "number" && opts.thresholdFreePercent > 0
      ? Math.floor(opts.thresholdFreePercent)
      : DEFAULT_THRESHOLD_FREE_PERCENT;
  const runLog = BIND_LOG_PURGE_RUN_LOG;

  return [
    "#!/bin/bash",
    "# Generated by hdc bind maintain/deploy — do not edit manually",
    "set -euo pipefail",
    `THRESHOLD=${threshold}`,
    `RUN_LOG=${JSON.stringify(runLog)}`,
    "",
    "log() {",
    '  echo "$(date -Is) $*" >> "$RUN_LOG"',
    "}",
    "",
    "root_free_percent() {",
    "  df -P / | awk 'NR==2 { gsub(/%/, \"\", $5); print 100 - $5 }'",
    "}",
    "",
    'FREE="$(root_free_percent)"',
    'if [ -z "$FREE" ]; then',
    '  log "error: could not determine root free percent"',
    "  exit 1",
    "fi",
    "",
    'if [ "$FREE" -ge "$THRESHOLD" ]; then',
    '  log "ok: ${FREE}% free (threshold ${THRESHOLD}%) — no purge"',
    "  exit 0",
    "fi",
    "",
    'log "purge: ${FREE}% free < ${THRESHOLD}% — reclaiming space"',
    "",
    "journalctl --vacuum-size=50M 2>/dev/null || true",
    "export DEBIAN_FRONTEND=noninteractive",
    "apt-get clean 2>/dev/null || true",
    "rm -rf /var/cache/apt/archives/partial/* 2>/dev/null || true",
    "",
    "shopt -s nullglob",
    'syslog_rotations=(/var/log/syslog.[0-9]* /var/log/syslog.[0-9]*.gz)',
    'if [ "${#syslog_rotations[@]}" -gt 0 ]; then',
    "  mapfile -t sorted < <(printf '%s\\n' \"${syslog_rotations[@]}\" | sort -V)",
    '  remove_count=$((${#sorted[@]} > 2 ? ${#sorted[@]} - 2 : 0))',
    '  if [ "$remove_count" -gt 0 ]; then',
    '    for ((i=0; i<remove_count; i++)); do',
    '      rm -f "${sorted[$i]}" && log "removed ${sorted[$i]}"',
    "    done",
    "  fi",
    "fi",
    "",
    "find /var/log -maxdepth 1 -name '*.gz' -mtime +7 -delete 2>/dev/null || true",
    "",
    'FREE="$(root_free_percent)"',
    'if [ -n "$FREE" ] && [ "$FREE" -lt "$THRESHOLD" ]; then',
    "  /usr/sbin/logrotate -f /etc/logrotate.d/rsyslog 2>/dev/null || true",
    "fi",
    "",
    'FREE_AFTER="$(root_free_percent)"',
    'log "done: free ${FREE_AFTER}% (was ${FREE}%, threshold ${THRESHOLD}%)"',
    "",
  ].join("\n");
}

/**
 * @param {object} opts
 * @param {number} opts.hour
 * @param {number} opts.minute
 * @param {string} [opts.scriptPath]
 * @param {string} [opts.runLog]
 */
export function buildBindLogPurgeCron(opts) {
  const hour = Number(opts.hour);
  const minute = Number(opts.minute);
  const scriptPath = opts.scriptPath ?? BIND_LOG_PURGE_SCRIPT_PATH;
  const runLog = opts.runLog ?? BIND_LOG_PURGE_RUN_LOG;
  const cronLine = `${minute} ${hour} * * * root ${scriptPath} >> ${runLog} 2>&1`;

  return [
    "# Generated by hdc bind maintain/deploy — do not edit manually",
    "SHELL=/bin/bash",
    "PATH=/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin",
    "CRON_TZ=UTC",
    cronLine,
    "",
  ].join("\n");
}

/**
 * @param {object} opts
 * @param {import("../../postfix-relay/lib/postfix-relay-configure.mjs").ConfigureExec} opts.exec
 * @param {{ info: (msg: string) => void; warn?: (msg: string) => void }} opts.log
 * @param {string} [opts.systemId]
 * @param {Record<string, string>} [opts.flags]
 * @param {unknown} [opts.bindBlock]
 */
export function ensureBindLogPurgeSchedule(opts) {
  if (logPurgeSkippedByFlags(opts.flags)) {
    opts.log.info(`${opts.exec.label}: BIND log purge cron skipped (--skip-log-purge)`);
    return { ok: true, skipped: true, message: "skipped by flag" };
  }

  const settings = resolveBindLogPurgeSettings(opts.bindBlock);
  if (!settings.enabled) {
    opts.log.info(`${opts.exec.label}: BIND log purge cron disabled in config`);
    return { ok: true, skipped: true, message: "disabled in config" };
  }

  const systemId = String(opts.systemId ?? "unknown").trim() || "unknown";
  const { hour, minute } = staggerOffsetFromSystemId(systemId, 1440);
  const script = buildBindLogPurgeScript({ thresholdFreePercent: settings.thresholdFreePercent });
  const cron = buildBindLogPurgeCron({ hour, minute });
  const scriptPath = BIND_LOG_PURGE_SCRIPT_PATH;
  const cronPath = BIND_LOG_PURGE_CRON_PATH;

  const installScript = [
    "set -e",
    `mkdir -p /usr/local/sbin`,
    `cat > '${scriptPath}' <<'HDC_BIND_LOG_PURGE_SCRIPT_EOF'`,
    script,
    "HDC_BIND_LOG_PURGE_SCRIPT_EOF",
    `chmod 755 '${scriptPath}'`,
    `cat > '${cronPath}' <<'HDC_BIND_LOG_PURGE_CRON_EOF'`,
    cron,
    "HDC_BIND_LOG_PURGE_CRON_EOF",
    `chmod 644 '${cronPath}'`,
  ].join("\n");

  opts.log.info(
    `${opts.exec.label}: ensuring BIND log purge cron (daily ${String(hour).padStart(2, "0")}:${String(minute).padStart(2, "0")} UTC, threshold ${settings.thresholdFreePercent}% free)`,
  );
  const r = opts.exec.run(installScript, { capture: true });
  if (r.status !== 0) {
    const detail = `${r.stderr}${r.stdout}`.trim() || `exit ${r.status}`;
    return { ok: false, skipped: false, message: detail };
  }

  return {
    ok: true,
    skipped: false,
    message: `daily at ${String(hour).padStart(2, "0")}:${String(minute).padStart(2, "0")} UTC`,
    cron: `${minute} ${hour} * * *`,
    threshold_free_percent: settings.thresholdFreePercent,
    script_path: scriptPath,
    cron_path: cronPath,
  };
}
