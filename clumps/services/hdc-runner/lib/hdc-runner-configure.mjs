import { readFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { renderAllCronFiles, sanitizeScheduleId } from "./hdc-runner-render-cron.mjs";
import { resolveScheduleDiscord, resolveScheduleMail } from "./hdc-runner-settings.mjs";
import { configureWebUiOnGuest } from "./hdc-runner-configure-ui.mjs";
import { configurePaperclipBridgeOnGuest } from "./hdc-runner-configure-bridge.mjs";

const JOB_SOURCE = join(dirname(fileURLToPath(import.meta.url)), "hdc-runner-job.mjs");
const AGENT_MANAGER_SOURCE = join(dirname(fileURLToPath(import.meta.url)), "run-agent-manager.mjs");
const AGENT_TASK_SOURCE = join(dirname(fileURLToPath(import.meta.url)), "run-agent-task.mjs");

/**
 * @param {Record<string, string>} envMap
 */
export function formatRunnerEnvValue(value) {
  const v = String(value ?? "");
  if (/^[A-Za-z0-9._@+-]+$/.test(v)) return v;
  return JSON.stringify(v);
}

export function renderRunnerDotEnv(envMap) {
  const lines = [
    "# Generated by hdc-runner maintain — runner meta only (paths, vault unlock).",
    "# Global + package .env files are synced under install_root and hdc-private.",
    "# HDC_OPS_DISCORD_WEBHOOK_URL is pushed here during maintain (not in config.json).",
  ];
  for (const [key, value] of Object.entries(envMap)) {
    if (!key || value === undefined || value === null) continue;
    lines.push(`${key}=${formatRunnerEnvValue(value)}`);
  }
  lines.push("");
  return lines.join("\n");
}

/**
 * Build schedules.json payload with resolved mail per schedule.
 *
 * @param {ReturnType<typeof import("./hdc-runner-settings.mjs").normalizeHdcRunnerBlock>} runner
 */
export function buildSchedulesJson(runner) {
  const schedules = runner.schedules.map((sched) => {
    const id = typeof sched.id === "string" ? sched.id.trim() : "";
    const type = typeof sched.type === "string" ? sched.type.trim() : "cli";
    return {
      id,
      type,
      cron: sched.cron,
      cli: sched.cli,
      cli_args: sched.cli_args ?? [],
      agent_role: typeof sched.agent_role === "string" ? sched.agent_role.trim() : undefined,
      resolved_mail: resolveScheduleMail(runner, sched),
      resolved_discord: resolveScheduleDiscord(runner, sched),
    };
  });
  return JSON.stringify({ schedules }, null, 2);
}

/**
 * Schedule ids retained when pruning stale cron files.
 *
 * @param {ReturnType<typeof import("./hdc-runner-settings.mjs").normalizeHdcRunnerBlock>} runner
 */
export function buildPruneKeepScheduleIds(runner) {
  return runner.schedules.map((s) => sanitizeScheduleId(String(s.id ?? "")));
}

/**
 * @param {import("../../postfix-relay/lib/postfix-relay-configure.mjs").ConfigureExec} exec
 * @param {ReturnType<typeof import("./hdc-runner-settings.mjs").normalizeHdcRunnerBlock>} runner
 * @param {Record<string, string>} envMap
 * @param {{ info: (msg: string) => void }} log
 * @param {{ systemId?: string; skipUi?: boolean }} [opts]
 */
export function configureHdcRunnerOnGuest(exec, runner, envMap, log, opts = {}) {
  const meta = runner.meta_root;
  const jobDest = `${meta}/bin/run-scheduled-job.mjs`;
  const agentManagerDest = `${meta}/bin/run-agent-manager.mjs`;
  const jobBody = readFileSync(JOB_SOURCE, "utf8");
  const agentManagerBody = readFileSync(AGENT_MANAGER_SOURCE, "utf8");
  const agentTaskBody = readFileSync(AGENT_TASK_SOURCE, "utf8");
  const systemId = typeof opts.systemId === "string" ? opts.systemId.trim() : "";
  const dotEnv = renderRunnerDotEnv({
    ...envMap,
    HDC_RUNNER_META_ROOT: meta,
    HDC_RUNNER_INSTALL_ROOT: runner.install_root,
    HDC_RUNNER_PRIVATE_ROOT: runner.private_root,
    HDC_PRIVATE_ROOT: runner.private_root,
    ...(systemId ? { HDC_OPS_DISCORD_HOST: systemId } : {}),
  });
  const schedulesJson = buildSchedulesJson(runner);
  const cronFiles = renderAllCronFiles(runner.schedules, {
    jobScriptPath: jobDest,
    logDir: "/var/log/hdc-runner",
    tz: runner.cron_tz,
  });

  const vwUrl = envMap.HDC_VAULTWARDEN_URL ?? "";

  /** @type {string[]} */
  const scriptParts = [
    "set -e",
    `mkdir -p '${meta}/bin' /var/log/hdc-runner /var/log/hdc-runner/agents`,
    `chown -R hdc:hdc '${meta}' /var/log/hdc-runner 2>/dev/null || true`,
    `cat > '${meta}/.env' <<'HDC_RUNNER_ENV_EOF'`,
    dotEnv,
    "HDC_RUNNER_ENV_EOF",
    `chmod 600 '${meta}/.env'`,
    `chown hdc:hdc '${meta}/.env' 2>/dev/null || true`,
    `cat > '${meta}/schedules.json' <<'HDC_RUNNER_SCHEDULES_EOF'`,
    schedulesJson,
    "HDC_RUNNER_SCHEDULES_EOF",
    `chown hdc:hdc '${meta}/schedules.json' 2>/dev/null || true`,
    `cat > '${jobDest}' <<'HDC_RUNNER_JOB_EOF'`,
    jobBody,
    "HDC_RUNNER_JOB_EOF",
    `chmod 755 '${jobDest}'`,
    `chown hdc:hdc '${jobDest}' 2>/dev/null || true`,
    `cat > '${agentManagerDest}' <<'HDC_RUNNER_AGENT_MGR_EOF'`,
    agentManagerBody,
    "HDC_RUNNER_AGENT_MGR_EOF",
    `chmod 755 '${agentManagerDest}'`,
    `chown hdc:hdc '${agentManagerDest}' 2>/dev/null || true`,
    `cat > '${meta}/bin/run-agent-task.mjs' <<'HDC_RUNNER_AGENT_TASK_EOF'`,
    agentTaskBody,
    "HDC_RUNNER_AGENT_TASK_EOF",
    `chmod 755 '${meta}/bin/run-agent-task.mjs'`,
    `chown hdc:hdc '${meta}/bin/run-agent-task.mjs' 2>/dev/null || true`,
  ];

  if (vwUrl) {
    scriptParts.push(`su - hdc -c "bw logout" 2>/dev/null || true`);
    scriptParts.push(`su - hdc -c "bw config server ${vwUrl.replace(/"/g, '\\"')}" || bw config server ${vwUrl.replace(/"/g, '\\"')}`);
  }

  for (const file of cronFiles) {
    scriptParts.push(
      `cat > '${file.path}' <<'HDC_RUNNER_CRON_EOF'`,
      file.content,
      "HDC_RUNNER_CRON_EOF",
      `chmod 644 '${file.path}'`,
    );
  }

  log.info(`${exec.label}: pushing .env, schedules, job wrapper, ${cronFiles.length} cron file(s)`);
  const r = exec.run(scriptParts.join("\n"), { capture: true });
  if (r.status !== 0) {
    const detail = `${r.stderr}${r.stdout}`.trim() || `exit ${r.status}`;
    return { ok: false, message: detail, cron_files: cronFiles.map((f) => f.path) };
  }

  const uiResult = configureWebUiOnGuest(exec, runner, { skipUi: opts.skipUi });
  if (!uiResult.ok) {
    return {
      ok: false,
      message: `web UI: ${uiResult.message}`,
      cron_files: cronFiles.map((f) => f.path),
    };
  }

  const skipBridge = opts.skipBridge === true;
  const bridgeResult = configurePaperclipBridgeOnGuest(exec, runner, { skipBridge });
  if (!bridgeResult.ok) {
    return {
      ok: false,
      message: `paperclip bridge: ${bridgeResult.message}`,
      cron_files: cronFiles.map((f) => f.path),
    };
  }

  return {
    ok: true,
    message: "configure complete",
    cron_files: cronFiles.map((f) => f.path),
    web_ui: uiResult,
    paperclip_bridge: bridgeResult,
  };
}

/**
 * Remove cron files not in config when --prune.
 *
 * @param {import("../../postfix-relay/lib/postfix-relay-configure.mjs").ConfigureExec} exec
 * @param {ReturnType<typeof import("./hdc-runner-settings.mjs").normalizeHdcRunnerBlock>} runner
 */
export function pruneStaleCronFiles(exec, runner) {
  const keep = new Set(buildPruneKeepScheduleIds(runner));
  const script = [
    "set -e",
    "for f in /etc/cron.d/hdc-runner-*; do",
    "  [ -e \"$f\" ] || continue",
    "  base=$(basename \"$f\" .conf 2>/dev/null || basename \"$f\")",
    "  base=${base%.conf}",
    "  case \"$base\" in hdc-runner-*) ;; *) continue ;; esac",
    "  id=${base#hdc-runner-}",
    `  keep='${[...keep].join(" ")}'`,
    "  found=0",
    "  for k in $keep; do [ \"$k\" = \"$id\" ] && found=1; done",
    "  [ \"$found\" = 1 ] || rm -f \"$f\"",
    "done",
  ].join("\n");
  const r = exec.run(script, { capture: true });
  if (r.status !== 0) {
    return { ok: false, message: `${r.stderr}${r.stdout}`.trim() || `exit ${r.status}` };
  }
  return { ok: true, message: "prune complete" };
}
