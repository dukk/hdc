/**
 * Cron file generation for hdc-runner schedules.
 */

/** @param {string} id */
export function sanitizeScheduleId(id) {
  const s = String(id ?? "").trim().toLowerCase();
  if (!/^[a-z0-9][a-z0-9-]{0,62}$/.test(s)) {
    throw new Error(`invalid schedule id ${JSON.stringify(id)} (use lowercase letters, digits, hyphen)`);
  }
  return s;
}

/**
 * Basic validation for 5-field cron (minute hour dom month dow).
 *
 * @param {string} cron
 */
export function validateCronExpression(cron) {
  const parts = String(cron ?? "").trim().split(/\s+/);
  if (parts.length !== 5) {
    throw new Error(`cron must have 5 fields (got ${JSON.stringify(cron)})`);
  }
  return parts.join(" ");
}

/**
 * @param {object} opts
 * @param {string} opts.scheduleId
 * @param {string} opts.cron
 * @param {string} opts.jobScriptPath
 * @param {string} [opts.logPath]
 */
export function renderCronFileContent(opts) {
  const scheduleId = sanitizeScheduleId(opts.scheduleId);
  const cron = validateCronExpression(opts.cron);
  const jobScript = String(opts.jobScriptPath ?? "").trim();
  if (!jobScript) throw new Error("jobScriptPath required");
  const logPath =
    typeof opts.logPath === "string" && opts.logPath.trim()
      ? opts.logPath.trim()
      : `/var/log/hdc-runner/${scheduleId}.log`;

  return [
    "# Generated by hdc-runner maintain — do not edit manually",
    "SHELL=/bin/bash",
    "PATH=/usr/local/bin:/usr/bin:/bin",
    `CRON_TZ=${opts.tz ?? "UTC"}`,
    `${cron} hdc /usr/bin/node ${jobScript} ${scheduleId} >> ${logPath} 2>&1`,
    "",
  ].join("\n");
}

/**
 * @param {string} scheduleId
 */
export function cronFileBasename(scheduleId) {
  return `hdc-runner-${sanitizeScheduleId(scheduleId)}`;
}

/**
 * @param {string} scheduleId
 */
export function cronFilePath(scheduleId) {
  return `/etc/cron.d/${cronFileBasename(scheduleId)}`;
}

/**
 * @param {Record<string, unknown>[]} schedules
 * @param {object} paths
 * @param {string} paths.jobScriptPath
 * @param {string} [paths.logDir]
 */
export function renderAllCronFiles(schedules, paths) {
  const logDir = paths.logDir ?? "/var/log/hdc-runner";
  /** @type {{ id: string; path: string; content: string }[]} */
  const files = [];
  const seen = new Set();
  for (const sched of schedules) {
    const id = typeof sched.id === "string" ? sched.id.trim() : "";
    if (!id) continue;
    const sid = sanitizeScheduleId(id);
    if (seen.has(sid)) throw new Error(`duplicate schedule id ${JSON.stringify(id)}`);
    seen.add(sid);
    const cron = typeof sched.cron === "string" ? sched.cron : "";
    files.push({
      id: sid,
      path: cronFilePath(sid),
      content: renderCronFileContent({
        scheduleId: sid,
        cron,
        jobScriptPath: paths.jobScriptPath,
        logPath: `${logDir}/${sid}.log`,
      }),
    });
  }
  return files;
}
